    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        // TODO add your handling code here:
        String url=txtUrl.getText();
        String pass=txtPass.getText();
        String driver=txtDriver.getText();
        String user=txtUser.getText();
        PropertyWriter pw=new PropertyWriter();
        try {
            pw.writeForKey(user, pass, driver, url);
            JOptionPane.showMessageDialog(this, "Uspešna izmena podešavanja.");
            primeniMod(EnumModForme.SHOW);
        } catch (IOException ex) {
            Logger.getLogger(FrmPodesavanja.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }//GEN-LAST:event_btnSacuvajActionPerformed


