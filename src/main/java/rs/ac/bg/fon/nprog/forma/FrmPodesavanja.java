    private void srediFormu() {
        pr = new PropertyReader();
        txtDriver.setText(pr.readForKey(Konstante.DRIVER));
        txtUrl.setText(pr.readForKey(Konstante.URL));
        txtUser.setText(pr.readForKey(Konstante.USER));
        txtPass.setText(pr.readForKey(Konstante.PASSWORD));
    }

    private void primeniMod(EnumModForme mod) {
        switch(mod){
            case SHOW:
                txtUrl.setEnabled(false);
                txtDriver.setEnabled(false);
                txtUser.setEnabled(false);
                txtPass.setEnabled(false);
                
                btnIzmeni.setVisible(true);
                btnSacuvaj.setVisible(false);
                break;
            case EDIT:
                txtUrl.setEnabled(true);
                txtDriver.setEnabled(true);
                txtUser.setEnabled(true);
                txtPass.setEnabled(true);
                
                btnIzmeni.setVisible(false);
                btnSacuvaj.setVisible(true);
                break;
        }
    }
